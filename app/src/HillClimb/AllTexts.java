package HillClimb;

public class AllTexts {

    private String ZT;
    private String decryptZT;
    private Double score;
    private int pemLength;
    private Integer[] permutation;

    public String getZT() {
        return ZT;
    }

    public void setZT(String ZT) {
        this.ZT = ZT;
    }

    public String getDecryptZT() {
        return decryptZT;
    }

    public void setDecryptZT(String decryptZT) {
        this.decryptZT = decryptZT;
    }

    public Double getScore() {
        return score;
    }

    public void setScore(Double score) {
        this.score = score;
    }

    public int getPemLength() {
        return pemLength;
    }

    public void setPemLength(int pemLength) {
        this.pemLength = pemLength;
    }

    public Integer[] getPermutation() {
        return permutation;
    }

    public void setPermutation(Integer[] permutation) {
        this.permutation = permutation;
    }
}
